def fmt_br(x: float | None) -> str:
    if x is None:
        return "---"
    if x >= 0.01:
        return f"{100.0 * x:.2f}\\%"
    return f"{100.0 * x:.3f}\\%"


def fmt_sigma(x: float | None) -> str:
    if x is None:
        return "---"
    if x >= 0.01:
        return f"{100.0 * x:.2f}\\%"
    if x >= 0.001:
        return f"{100.0 * x:.3f}\\%"
    return f"{100.0 * x:.4f}\\%"


def fmt_ns(x: float | None) -> str:
    if x is None:
        return "---"
    if x >= 100:
        return f"{x:.0f}"
    if x >= 10:
        return f"{x:.1f}"
    return f"{x:.2f}"


def fmt_qm(x: float | None) -> str:
    if x is None:
        return "---"
    return f"{x:.3f}"


def fmt_fraction_pct(x: float) -> str:
    return f"{100.0 * x:.0f}\\%"


def _latex_sanitize(text: str) -> str:
    replacements = {
        "π": "$\\pi$",
        "ρ": "$\\rho$",
        "ω": "$\\omega$",
        "η": "$\\eta$",
        "→": "$\\to$",
        "±": "$\\pm$",
    }
    out = text
    for src, dst in replacements.items():
        out = out.replace(src, dst)
    return out.replace("_", "\\_").replace("%", "\\%")


def _mass_floor_metrics(rows: list[dict], floor_frac: float) -> dict[str, Any]:
    listed_ns: list[float] = []
    floored_ns: list[float] = []
    for row in rows:
        pdg = row.get("pdg_mev")
        sigma = row.get("sigma_mev")
        delta = row.get("delta_mev")
        if pdg is None or sigma is None or sigma <= 0 or delta is None:
            continue
        ad = abs(float(delta))
        listed_ns.append(ad / float(sigma))
        cmp_sigma = max(float(sigma), floor_frac * float(pdg))
        floored_ns.append(ad / cmp_sigma)

    def band(ns: list[float], k: float) -> int:
        return sum(1 for x in ns if x <= k)

    n = len(floored_ns)
    return {
        "floor_frac": floor_frac,
        "n": n,
        "within_1sigma_listed": band(listed_ns, 1.0),
        "within_2sigma_listed": band(listed_ns, 2.0),
        "within_3sigma_listed": band(listed_ns, 3.0),
        "within_1sigma_floored": band(floored_ns, 1.0),
        "within_2sigma_floored": band(floored_ns, 2.0),
        "within_3sigma_floored": band(floored_ns, 3.0),
        "max_n_sigma_listed": max(listed_ns) if listed_ns else None,
        "max_n_sigma_floored": max(floored_ns) if floored_ns else None,
    }


