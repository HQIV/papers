def _fmt_halflife_seconds(t: float | None) -> str:
    if t is None or not math.isfinite(t):
        return "---"
    if t >= 1e-3:
        return f"${t * 1e3:.3f}\\,\\mathrm{{ms}}$"
    if t >= 1e-6:
        return f"${t * 1e6:.3f}\\,\\mathrm{{\\mu s}}$"
    if t >= 1e-9:
        return f"${t * 1e9:.3f}\\,\\mathrm{{ns}}$"
    return f"${t:.3e}\\,\\mathrm{{s}}$"


