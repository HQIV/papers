def _observation_by_id(observations: dict) -> dict[str, dict]:
    return {
        str(item["id"]): item
        for item in observations.get("branching_comparison_panel") or []
    }


def _daughter_ids_for_item(item: dict) -> list[str]:
    if item.get("daughter_ids"):
        return [str(d) for d in item["daughter_ids"]]
    sets = item.get("daughter_sets") or []
    if not sets:
        return []
    # Use the lightest daughter set as a conservative Q/M proxy for inclusive families.
    def mass_sum(dids: list) -> float:
        return sum(hep.particle_mass_mev(str(d)) for d in dids)

    return [str(d) for d in min(sets, key=mass_sum)]


def _q_over_m(item: dict) -> float | None:
    parent_id = str(item.get("parent_id", ""))
    daughters = _daughter_ids_for_item(item)
    if not parent_id or not daughters:
        return None
    parent_m = hep.particle_mass_mev(parent_id)
    if parent_m <= 0:
        return None
    daughter_m = sum(hep.particle_mass_mev(d) for d in daughters)
    q = parent_m - daughter_m
    return q / parent_m if q > 0 else 0.0


def _n_sigma_ref_only(predicted: float, reference: float, ref_sigma: float | None) -> float | None:
    if ref_sigma is None or ref_sigma <= 0:
        return None
    return abs(predicted - reference) / ref_sigma


