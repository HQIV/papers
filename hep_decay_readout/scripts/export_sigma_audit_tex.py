def _state_tex(row: dict) -> str:
    key = str(row.get("pdg_key") or "")
    mapping = {
        "J/psi": "$J/\\psi$",
        "psi2S": "$\\psi(2S)$",
        "Upsilon2S": "$\\Upsilon(2S)$",
        "chi_c1": "$\\chi_{c1}$",
        "D0": "$D^0$",
        "B0": "$B^0$",
        "B_plus": "$B^+$",
    }
    if key in mapping:
        return mapping[key]
    name = str(row.get("name", key))
    return name.replace("_", "\\_")


def build_listed_pull_extremes_tex(excited: dict) -> str:
    """Type-A rows where listed PDG σ makes formal n_σ ill-posed (>100σ)."""
    panel_rows = excited.get("pdg_panel_rows") or [
        r for r in excited.get("rows", []) if r.get("pdg_mev") and not r.get("hqiv_only")
    ]
    scored: list[dict] = []
    for row in panel_rows:
        listed = row.get("n_sigma_listed")
        if listed is None or listed <= 100.0:
            continue
        pdg = float(row["pdg_mev"])
        sigma = float(row.get("sigma_mev") or 0)
        sigma_over_m = 100.0 * sigma / pdg if pdg > 0 else 0.0
        scored.append({**row, "sigma_over_m_pct": sigma_over_m})
    scored.sort(key=lambda r: -float(r["n_sigma_listed"]))

    lines = [
        "% Auto-generated by scripts/export_sigma_audit_tex.py — do not edit by hand.",
        "\\begin{table}[ht]",
        "\\centering",
        "\\caption{Type~A listed-pull extremes ($n_\\sigma>100$ using raw PDG $\\sigma$). "
        "These six rows have $\\sigma/M\\ll 1\\%$ (often width-derived tags on narrow states); "
        "formal listed $n_\\sigma$ is ill-posed (table shows $\\gg 10^2$; CSV has numeric values). "
        "Scale-free $|\\Delta|/M$ and floored "
        "$\\tilde n_\\sigma$ (Table~\\ref{tab:excited-mass-comparison}) are the honest metrics.}",
        "\\label{tab:listed-pull-extremes}",
        "\\small",
        "\\begin{tabular}{@{}lrrrrr@{}}",
        "\\toprule",
        "State & $|\\Delta|/M$\\,\\% & $\\sigma/M$\\,\\% & $n_\\sigma$ (listed) "
        "& $\\tilde n_\\sigma$ (floored) & Class \\\\",
        "\\midrule",
    ]
    for row in scored:
        name = _state_tex(row)
        listed = float(row["n_sigma_listed"])
        floored = float(row.get("n_sigma_floored") or 0)
        pct = float(row.get("abs_error_pct") or 0)
        sig_pct = float(row["sigma_over_m_pct"])
        listed_s = "$\\gg 10^2$" if listed > 100.0 else f"{listed:.1f}"
        lines.append(
            f"{name} & {pct:.3f} & {sig_pct:.4f} & {listed_s} & {floored:.3f} & Type~A \\\\"
        )
    if not scored:
        lines.append("\\multicolumn{6}{c}{No rows with listed $n_\\sigma>100$.} \\\\")
    lines.extend(
        [
            "\\bottomrule",
            "\\end{tabular}",
            "\\end{table}",
            "",
        ]
    )
    return "\n".join(lines)


